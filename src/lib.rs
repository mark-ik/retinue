//! retinue — an endpoint-scoped implementation of the
//! [Reticulum](https://reticulum.network/) protocol.
//!
//! A retinue is the company that travels with a person. This crate aims to be
//! that for a peer: the identity, announce, link, and resource layers a node
//! needs to *be* a Reticulum endpoint, embedded as a library. Transport-node
//! routing is a non-goal.
//!
//! Status: name reservation + scaffold. Wire-compatibility target is RNS 1.3.x,
//! implemented from the public-domain protocol specification and validated
//! against the Python reference as a black-box interoperability oracle. See the
//! repository's `design_docs/` for the plan.

